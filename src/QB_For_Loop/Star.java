package QB_For_Loop;

public class Star {
	
	public static void main(String[] args) {
		
		//horizontal with space
		for(int a = 0; a<=5; a++) {
			System.out.println("* ");
			
		}
		
		System.out.println("=======================");
		
	//vertical with space
	for(int a = 0; a<=5;  a++) {
		System.out.print("* ");
		
	}
	System.out.println("==========================");
	
	
	//vertical 10 star
	for(int a = 0; a<=10; a++) {
		System.out.println("*");
		
	}
	System.out.println("========================");
	
	for(int a = 0; a<=10; a++) {
		System.out.print("*");

}
}
}
