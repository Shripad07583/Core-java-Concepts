package For_Loop;

public class Demo {
	public static void main(String[] args) {
		
		
		for(int a = 0; a<10; a++) {
			System.out.println(a);
			
		}
		System.out.println("------------------------");
		
	for(int a = 1; a<=10; a++) {
		int num = a*3;
		if(num%2==0) {
			System.out.println(num);
		}
	}

}
}